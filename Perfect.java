class Perfect 
{
	public static void main(String[] args) 
	{
     int num=5,sum=0;
	 for (int i=1;i<num ;i++ )
	 {
		 if (num%i==0)
		 {
			 sum=sum+i;
		 }
	 }
		
	if (sum==num)
	{
		System.out.println("it is an perfect num"+sum);
	}
	else
		{
		System.out.println("it is an perfect num"+sum);
		}
}
}
